-- =============================================================================
-- QuarryOps · Миграция 0013 — привязка водителей к договору
-- Приложение №2 договора — списки техники И водителей. У техники contract_id
-- уже есть; добавляем водителям. Используется для автоподбора водителя по
-- договору машины и (в будущем) генерации Приложения №2.
-- =============================================================================

alter table public.drivers
  add column contract_id uuid references public.contracts(id);

comment on column public.drivers.contract_id is
  'Договор, по которому водитель допущен (Приложение №2). null = без привязки.';

create index drivers_contract_idx on public.drivers (contract_id);
