-- tapped_at — время нажатия на телефоне учётчика; created_at — время доставки
-- на сервер (при обрыве сети outbox отправляет позже, и они расходятся).
-- Отображение везде — по tapped_at ?? created_at. Геолокация из формы убрана
-- (колонки geo_lat/geo_lng остаются для истории, новые записи пишут NULL).
-- =============================================================================

alter table public.trip_records
  add column if not exists tapped_at timestamptz;
comment on column public.trip_records.tapped_at is
  'Момент тапа на телефоне учётчика (клиентское время); NULL — старые записи.';
