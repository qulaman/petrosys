-- =============================================================================
-- QuarryOps · Миграция 0006 — флаг подписи водителя на рейсе (уровень маршрута)
-- По ТЗ подпись на каждой ходке замедляет поток → по умолчанию выключена,
-- включается на конкретном маршруте, если бухгалтерия/подрядчик требуют.
-- =============================================================================

alter table public.routes
  add column if not exists require_signature boolean not null default false;

comment on column public.routes.require_signature is
  'Требовать подпись водителя на каждом рейсе по этому маршруту (по умолчанию нет).';
