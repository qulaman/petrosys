-- =============================================================================
-- QuarryOps · Миграция 0025 — удаление карточки смены рейсов
-- Учётчик может удалить ОТКРЫТУЮ карточку (ошибочно созданную), офис/админ —
-- любую. Рейсы карточки удаляются приложением до самой карточки (FK без
-- каскада защищает от случайного сноса рейсов); перечень машин уходит каскадом.
-- =============================================================================

create policy "delete trip_lineups"
  on public.trip_lineups for delete to authenticated
  using (
    org_id = public.current_org_id()
    and (public.has_any_role(array['office', 'admin'])
         or (public.has_any_role(array['checker']) and status = 'open'))
  );
