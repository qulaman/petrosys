-- =============================================================================
-- QuarryOps · Миграция 0021 — SVG-подписи разрешены в бакете signatures
--
-- Хотфикс к 0020: подписи стали image/svg+xml, но бакет создавался с
-- allowed_mime_types = ['image/png'] — Storage отклонял загрузку
-- («не удалось загрузить подпись» у водителя). PNG остаётся разрешённым
-- как переходный фолбэк старых открытых вкладок.
-- =============================================================================

update storage.buckets
   set allowed_mime_types = array['image/png', 'image/svg+xml']
 where id = 'signatures';
