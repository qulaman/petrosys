-- =============================================================================
-- QuarryOps · Миграция 0020 — лимиты размера файлов в Storage
--
-- Подписи стали вектором (SVG ~2 КБ), чеки сжимаются на клиенте (~250 КБ).
-- Лимиты — предохранитель от регрессий кода, чтобы в бакеты снова не полились
-- мегабайты: подпись 200 КБ (PNG-фолбэк старых вкладок проходит), чек 5 МБ
-- (фолбэк «оригинал с камеры» при ошибке сжатия обязан пройти — запись важнее).
-- =============================================================================

update storage.buckets set file_size_limit = 204800  where id = 'signatures';
update storage.buckets set file_size_limit = 5242880 where id = 'receipts';
