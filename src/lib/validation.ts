import { z } from "zod";

/**
 * Мягкая проверка формата UUID (8-4-4-4-12 hex).
 * В zod v4 `z.string().uuid()` строго валидирует version/variant по RFC 9562
 * и отвергает «читаемые» сид-идентификаторы (напр. 70000000-0000-0000-0000-000000000001).
 * Идентификаторы приходят из нашей БД — строгая семантическая проверка не нужна,
 * достаточно формата.
 */
export const zUuid = z
  .string()
  .regex(
    /^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$/,
    "Некорректный идентификатор",
  );
